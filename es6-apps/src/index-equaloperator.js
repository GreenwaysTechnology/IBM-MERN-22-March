let x = "100";
let y = 100;
if (x == y) {
    console.log('X and y equal')
} else {
    console.log('X and y not equal')
}

// === tests both value and type
if (x === y) {
    console.log('X and y equal')
} else {
    console.log('X and y not equal')
}